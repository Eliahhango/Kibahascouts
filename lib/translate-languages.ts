export function detectLanguageFromCountry(countryCode: string | null | undefined): SupportedLanguage | null {
  if (!countryCode) return null
  const mapped = countryLanguageMap[countryCode.trim().toUpperCase()]
  return mapped ?? null
}

export function detectLanguageFromAcceptLanguage(headerValue: string | null | undefined): SupportedLanguage | null {
  if (!headerValue) return null

  const candidates = headerValue
    .split(",")
    .map((item) => item.split(";")[0]?.trim())
    .filter(Boolean)

  for (const candidate of candidates) {
    const normalized = normalizeLanguageCode(candidate)
    if (normalized) return normalized
  }

  return null
}
