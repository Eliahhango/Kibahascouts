type SupportedLanguage = (typeof languageCodes)[number]

const supportedLowerMap = new Map<string, SupportedLanguage>(
  languageCodes.map((code) => [code.toLowerCase(), code]),
)

export function normalizeLanguageCode(input: string | null | undefined): SupportedLanguage | null {
  if (!input) return null
  const value = input.trim().toLowerCase()
  if (!value) return null

  if (value === "zh" || value.startsWith("zh-cn") || value.startsWith("zh-sg")) return "zh-CN"
  if (value.startsWith("zh-tw") || value.startsWith("zh-hk") || value.startsWith("zh-mo")) return "zh-TW"

  const direct = supportedLowerMap.get(value)
  if (direct) return direct

  const base = value.split("-")[0]
  return supportedLowerMap.get(base) ?? null
}

